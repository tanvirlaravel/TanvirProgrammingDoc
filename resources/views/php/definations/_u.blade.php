  <h3>Unix Epoch</h3>
  <hr>
  <p>The Unix epoch is the number of seconds that have elapsed since January 1, 1970 at midnight UTC time minus the leap seconds. This means that at midnight of January 1, 1970, Unix time was 0.</p>
  <p>The Unix epoch is also called Unix time, POSIX time, or Unix timestamp</p>
  
